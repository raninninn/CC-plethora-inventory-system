chest1 = peripheral.wrap(&quot;minecraft:ironchest_copper_5&quot;)
bRunning = true

local tChests = {chest0, chest1}

local function main()
    -- Prompt
    write(&quot;? &quot;)
    input = io.read()
    input = input:gsub(&quot;%s+&quot;, &quot;&quot;)
    
    -- Commands
    if input == &quot;cat&quot; then
        local sOut = &quot;&quot;
        local b = {}
        local maxb = 1
        for k,chest in pairs(tChests) do
            local a = chest.list()
                  
            for i=1, #a do
                if a[i] ~= nil then
                    local id = chest.getItemMeta(i).rawName
                    if b[id] ~= nil then
                        b[id] = chest.getItemMeta(i).count + b[id]
                    else
                        b[id] = chest.getItemMeta(i).count
                    end
                
                    if string.len( tostring(b[id]) ) &gt; maxb then
                        maxb = string.len( tostring(b[id]) )
                    end
                end
            end
            for i=1, #a do
                if a[i] ~= nil then
                    local name = chest.getItemMeta(i).displayName
                    local id = chest.getItemMeta(i).rawName
                    local bid_len = string.len( tostring(b[id]) )
                    
                    local padding = &quot;&quot;
                    
                    if sOut:match(name) == nil then
                        for j=1, maxb-bid_len do
                            padding = padding .. &quot; &quot;
                        end
                        sOut = sOut..padding..b[id] ..&quot;  &quot;.. name..&quot;\n&quot;
                    end
                end
            end
        end
        textutils.pagedPrint(sOut)
        local file = fs.open(&quot;item_catalogue&quot;, &quot;w&quot;)
        file.write(sOut)
    end
    if input == &quot;quit&quot; then
        bRunning = false
